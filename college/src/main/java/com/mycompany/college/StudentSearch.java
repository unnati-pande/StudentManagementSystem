    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String studentRollNum=sRollNumber.getText();
        DefaultTableModel model=(DefaultTableModel)Table.getModel();
        if(studentRollNum.isEmpty()){
            JOptionPane.showMessageDialog(this,"please enter roll num! ");
        }
        try{
            model.setRowCount(0);
            //load driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            //create connection
            String url="jdbc:mysql://127.0.0.1:3306/college";
            String username="root";
            String password="unnati@180604";
            Connection con=DriverManager.getConnection(url, username, password);
            String query="select * from student where rollnumber=?";
            PreparedStatement pstmt=con.prepareStatement(query);
            pstmt.setString(1, studentRollNum);
            ResultSet rs=pstmt.executeQuery();
            boolean has_result=false;
            while(rs.next()){
                has_result=true;
                int srollnumber=rs.getInt("rollnumber");
                String student_rollnum=Integer.toString(srollnumber);
                String student_name=rs.getString("name");
                String student_class=rs.getString("Class");
                String row[]={student_rollnum, student_name, student_class};
                model.addRow(row);
            }
            if(!has_result){
                JOptionPane.showMessageDialog(this, "no result found");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

