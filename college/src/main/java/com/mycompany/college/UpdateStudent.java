    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String student_name=sname.getText();
        String student_class=sclass.getText();
        String s_rollnum=srollnumber.getText();
        int student_rollnumber=Integer.parseInt(s_rollnum);
        try{
            //load driver
            Class.forName("com.mysql.cj.jdbc.Driver");
            //create connection
            String url="jdbc:mysql://127.0.0.1:3306/college";
            String username="root";
            String password="unnati@180604";
            Connection con=DriverManager.getConnection(url, username, password);
            String query="update student set name=?, Class=? where rollnumber=?";
            PreparedStatement pstmt=con.prepareStatement(query);
            pstmt.setString(1, student_name);
            pstmt.setString(2, student_class);
            pstmt.setInt(3, student_rollnumber);
            pstmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "updated successfully");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

